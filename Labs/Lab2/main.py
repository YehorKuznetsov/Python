# Запитуємо у користувача температуру в градусах Цельсія
temperature_input = input("Введіть температуру в градусах Цельсія: ")

try:
    # Перетворюємо введене значення в число типу float
    celsius_temperature = float(temperature_input)

    # Обчислюємо температуру в градусах Фаренгейта за формулою
    fahrenheit_temperature = (celsius_temperature * 9 / 5) + 32

    # Виводимо результат
    print(f"Температура в градусах Фаренгейта: {fahrenheit_temperature}")

except ValueError:
    # Якщо введене значення не є числом, виводимо повідомлення про помилку
    print("Помилка: введіть правильне числове значення температури.")
